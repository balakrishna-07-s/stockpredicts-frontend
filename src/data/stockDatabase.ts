// Simple fuzzy search function
export function fuzzySearch(query: string, stocks: StockItem[], maxResults: number = 10): StockItem[] {
  if (!query.trim()) return [];
  
  const searchTerm = query.toLowerCase().trim();
  const results: { stock: StockItem; score: number }[] = [];
  
  stocks.forEach(stock => {
    const fullNameLower = stock.fullName.toLowerCase();
    const symbolLower = stock.symbol.toLowerCase();
    
    let score = 0;
    
    // Exact matches get highest score
    if (fullNameLower.includes(searchTerm) || symbolLower.includes(searchTerm)) {
      score += 100;
    }
    
    // Word boundary matches
    const words = fullNameLower.split(/\s+/);
    const searchWords = searchTerm.split(/\s+/);
    
    searchWords.forEach(searchWord => {
      words.forEach(word => {
        if (word.startsWith(searchWord)) {
          score += 50;
        }
        if (word.includes(searchWord)) {
          score += 20;
        }
      });
    });
    
    // Character-based fuzzy matching for spelling mistakes
    if (score === 0 && searchTerm.length >= 3) {
      const fullNameScore = calculateLevenshteinScore(searchTerm, fullNameLower);
      const symbolScore = calculateLevenshteinScore(searchTerm, symbolLower);
      score = Math.max(fullNameScore, symbolScore);
    }
    
    if (score > 0) {
      results.push({ stock, score });
    }
  });
  
  return results
    .sort((a, b) => b.score - a.score)
    .slice(0, maxResults)
    .map(result => result.stock);
}

function calculateLevenshteinScore(str1: string, str2: string): number {
  const distance = levenshteinDistance(str1, str2);
  const maxLength = Math.max(str1.length, str2.length);
  const similarity = 1 - (distance / maxLength);
  
  // Only return meaningful scores for close matches
  return similarity > 0.6 ? similarity * 30 : 0;
}

function levenshteinDistance(str1: string, str2: string): number {
  const matrix = Array(str2.length + 1).fill(null).map(() => Array(str1.length + 1).fill(null));
  
  for (let i = 0; i <= str1.length; i += 1) {
    matrix[0][i] = i;
  }
  
  for (let j = 0; j <= str2.length; j += 1) {
    matrix[j][0] = j;
  }
  
  for (let j = 1; j <= str2.length; j += 1) {
    for (let i = 1; i <= str1.length; i += 1) {
      const indicator = str1[i - 1] === str2[j - 1] ? 0 : 1;
      matrix[j][i] = Math.min(
        matrix[j][i - 1] + 1, // deletion
        matrix[j - 1][i] + 1, // insertion
        matrix[j - 1][i - 1] + indicator, // substitution
      );
    }
  }
  
  return matrix[str2.length][str1.length];
}
